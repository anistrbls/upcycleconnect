const normalize = (value) => String(value || "").replace(/\s+/g, " ").trim();

const escapeRegex = (value) => String(value).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

const hasLetterAtStart = (value) => /^\p{L}/u.test(value);
const hasLetterAtEnd = (value) => /\p{L}$/u.test(value);

const replaceFragment = (source, from, to) => {
    const before = hasLetterAtStart(from) ? "(?<![\\p{L}\\p{N}])" : "";
    const after = hasLetterAtEnd(from) ? "(?![\\p{L}\\p{N}])" : "";
    return source.replace(new RegExp(`${before}${escapeRegex(from)}${after}`, "gu"), to);
};

const cleanup = (value) => normalize(value)
    .replace(/\s+([:;,.!?])/g, "$1")
    .replace(/\(\s+/g, "(")
    .replace(/\s+\)/g, ")")
    .replace(/ +\*/g, " *");

const sortedFragments = [...fragments].sort((a, b) => b[0].length - a[0].length);

const translate = (source) => {
    const key = normalize(source);
    if (!key || codeLike.test(key)) return source;
    if (exact[key]) return exact[key];

    let output = key;
    for (const [from, to] of sortedFragments) {
        output = replaceFragment(output, from, to);
    }

    output = output
        .replace(/…/g, "…")
        .replace(/\bEUR\b/g, "EUR")
        .replace(/\b€\b/g, "€");

    return cleanup(output);
};

const translatedCount = (phrases) => Object.entries(phrases).filter(([source, value]) => normalize(source) !== normalize(value)).length;

const uniquePatterns = (patterns) => {
    const seen = new Set();
    return patterns.filter((pattern) => {
        const key = `${pattern.match}=>${pattern.replace}`;
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
    });
};

