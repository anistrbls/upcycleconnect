const phrases = catalog.phrases || {};
const before = translatedCount(phrases);

for (const source of Object.keys(phrases)) {
    const translated = translate(source);
    if (normalize(translated) !== normalize(source)) {
        phrases[source] = translated;
    }
}

for (const [source, translated] of Object.entries(exact)) {
    phrases[source] = translated;
}

catalog.phrases = Object.fromEntries(Object.entries(phrases).sort(([a], [b]) => a.localeCompare(b, "fr", { sensitivity: "base" })));
catalog.patterns = uniquePatterns([...(catalog.patterns || []), ...extraPatterns]);

await fs.writeFile(localeFile, `${JSON.stringify(catalog, null, 2)}\n`);

